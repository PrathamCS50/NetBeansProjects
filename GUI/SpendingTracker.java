    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            int ri = table.getSelectedRow();
            if (ri != -1) {
                
                int r = JOptionPane.showConfirmDialog(null, "Do You Really Wanna DELETE !!", "Deleteion Confirmation", JOptionPane.YES_NO_OPTION);
                if (r == JOptionPane.YES_OPTION) {
                    
                    int id = (int) table.getValueAt(ri, 0); // object to int typecast to child
                    db.DbConnect.st.executeUpdate("delete from spendings where sid = " + id);
                    JOptionPane.showMessageDialog(null, "SuccessFully Deleted");
                    getEntries();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

